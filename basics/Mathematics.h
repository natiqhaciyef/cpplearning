#include <iostream>
#include <cmath>

using namespace std;

#ifndef CPPLEARNING_MATHEMATICS_H
#define CPPLEARNING_MATHEMATICS_H

void mathFunctionsUsage() {
    // finds square root
    cout << sqrt(64) << endl;

    // rounds a number
    cout << round(3.14) << endl;

    // natural logarithm
    cout << log(2.2) << endl;

    // rounds up to it's the nearest number
    cout << ceil(34.1) << endl;

    // rounds down to it's the nearest number
    cout << floor(34.1) << endl;

    // power
    cout << pow(4, 2) << endl;
}


#endif //CPPLEARNING_MATHEMATICS_H
